package com.harshavardhang.weathertestdemo.model;

public class Wind {

    private double speed; // Wind speed in meters/second
    private int deg; // Wind direction in degrees

    // Getters and Setters
    public double getSpeed() {
        return speed;
    }

    public void setSpeed(double speed) {
        this.speed = speed;
    }

    public int getDeg() {
        return deg;
    }

    public void setDeg(int deg) {
        this.deg = deg;
    }
}
